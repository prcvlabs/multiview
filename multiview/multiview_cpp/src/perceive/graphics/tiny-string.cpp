static void tiny_char(unsigned char c,
                      std::function<void(unsigned x, unsigned y)> f);
static void tiny_char_mask(unsigned char c,
                           std::function<void(unsigned x, unsigned y)> f);
static int tiny_char_width();
static int tiny_char_height();

// Returns TRUE if a new line
static bool process_char(unsigned char c,
                         unsigned char mask,
                         std::function<void(unsigned x, unsigned y)> f)
{
   if(c > 127) return false;
   if(c == '\n') return true;
   const int oy = int(c) * 6;
   for(int y = 0; y < 5; ++y)
      for(int x = 0; x < 3; ++x)
         if(pixel_data[size_t(x + (y + oy) * 3)] == mask)
            f(unsigned(x), unsigned(y));
   return false;
}

// 3, 768,
static void tiny_char(unsigned char c,
                      std::function<void(unsigned x, unsigned y)> f)
{
   process_char(c, 1, f);
}

static void tiny_char_mask(unsigned char c,
                           std::function<void(unsigned x, unsigned y)> f)
{
   process_char(c, 0, f);
}

static int tiny_char_width() { return 3; }

static int tiny_char_height() { return 5; }

static void render_tiny(const string_view s_data,
                        unsigned char mask,
                        std::function<void(int x, int y)> f)
{
   int ox = 0;
   int oy = 0;
   auto s = s_data.data();
   while(*s) {
      if(mask == 0 && ox == 0) // draw bar at 0
         for(int y = -1; y <= tiny_char_height(); ++y) f(-1, y + oy);

      const bool is_newline = process_char(
          uint8_t(*s++), mask, [&](int x, int y) { f(x + ox, y + oy); });
      if(is_newline) {
         ox = 0;
         oy += tiny_char_height() + 2;
      }

      if(mask == 0) {
         // draw bar at top/bottom
         for(int x = 0; x < tiny_char_width(); ++x) f(ox + x, oy - 1);
         for(int x = 0; x < tiny_char_width(); ++x)
            f(ox + x, oy + tiny_char_height());

         // draw bar at right
         for(int y = -1; y <= tiny_char_height(); ++y)
            f(ox + tiny_char_width(), oy + y);
      }

      if(!is_newline) ox += tiny_char_width() + 1;
   }
}

// ----------------------------------------------------------------- Render Tiny

void render_tiny(const string_view s, std::function<void(int x, int y)> f)
{
   render_tiny(s, 1, f);
}

void render_tiny_mask(const string_view s, std::function<void(int x, int y)> f)
{
   render_tiny(s, 0, f);
}

void render_tiny_dimensions(const string_view s_data, int& width, int& height)
{
   int ox = 0;
   int oy = 0;

   width = height = 0;

   auto process_end = [&](const bool is_newline) {
      if(ox > width) width = ox;
      ox = 0;
      height += tiny_char_height() + 2;
   };

   auto s = s_data.data();
   while(*s) {
      const bool is_newline
          = process_char(uint8_t(*s++), 255, [&](int x, int y) {});
      if(is_newline)
         process_end(true);
      else
         ox += tiny_char_width() + 1;
   }

   process_end(false);
}

// ------------------------------------------------------------------ Render 8x8

static void process_char_8x8(unsigned char c,
                             unsigned char mask,
                             std::function<void(unsigned x, unsigned y)> f)
{
   if(c > 127) return;
   const int oy = int(c) * 6;
   for(int y = 0; y < 5; ++y)
      for(int x = 0; x < 3; ++x)
         if(pixel_data[unsigned(x + (y + oy) * 3)] == mask)
            f(unsigned(x), unsigned(y));
}

static void render_tiny_8x8(const string_view s_data,
                            unsigned char mask,
                            std::function<void(int x, int y)> f)
{
   int ox       = 0;
   const auto w = 8;
   const auto h = 8;
   auto s       = s_data.data();
   while(*s) {
      if(mask == 0 && ox == 0) // draw bar at 0
         for(int y = -1; y <= w; ++y) f(-1, y);

      process_char_8x8(uint8_t(*s), mask, [&](int x, int y) { f(x + ox, y); });

      if(mask == 0) {
         // draw bar at top/bottom
         for(int x = 0; x < w; ++x) f(ox + x, -1);
         for(int x = 0; x < w; ++x) f(ox + x, h);
         // draw bar at right
         for(int y = -1; y <= h; ++y) f(ox + w, y);
      }
      ox += w + 1;
      ++s;
   }
}

void render_tiny_8x8(const string_view s, std::function<void(int x, int y)> f)
{
   render_tiny_8x8(s, 1, f);
}

void render_tiny_mask_8x8(const string_view s,
                          std::function<void(int x, int y)> f)
{
   render_tiny_8x8(s, 0, f);
}

void render_tiny_dimensions_8x8(const string_view s, int& width, int& height)
{
   int len = int(s.size());
   width   = 1 + len * 8;
   height  = 9;
}

